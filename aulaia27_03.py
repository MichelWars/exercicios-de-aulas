import pandas as pd

#Criando uma Série:
# Criando uma Série a partir de uma lista
data = [10, 20, 30, 40]
series = pd.Series(data)
print(series)

# Criando uma Série com índices personalizados
index = ['a', 'b', 'c', 'd']
